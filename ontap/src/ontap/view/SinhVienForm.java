    public void loadData() {
        ArrayList<SinhVien> list = quanLySinhVienService.getAll();
        defaultTableModel = (DefaultTableModel) tblSinhVien.getModel();
        defaultTableModel.setRowCount(0);
        for (SinhVien sinhVien : list) {
            defaultTableModel.addRow(new Object[]{
                sinhVien.getId(), sinhVien.getTen(),
                sinhVien.getDiaChi(), sinhVien.hienThiTrangThai(sinhVien.getTrangThai())
            });

        }
    }

