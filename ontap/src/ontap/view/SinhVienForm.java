    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        Integer id = Integer.parseInt(txtId.getText());
        String hoTen = txtHoTen.getText();
        String diaChi = txtDiaChi.getText();
        String trangThai = (String) cboTrangThai.getSelectedItem();

        SinhVien sinhVienNew = new SinhVien();
        sinhVienNew.setId(id);
        sinhVienNew.setTen(hoTen);
        sinhVienNew.setDiaChi(diaChi);
        if (trangThai.equals("Passed")) {
            sinhVienNew.setTrangThai(1);
        } else {
            sinhVienNew.setTrangThai(0);
        }
        String kq = quanLySinhVienService.them(sinhVienNew);
        JOptionPane.showMessageDialog(this, kq);
        loadData();
    }//GEN-LAST:event_btnThemActionPerformed

